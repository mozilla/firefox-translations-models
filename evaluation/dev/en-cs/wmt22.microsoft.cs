Soukromé lyžařské středisko určuje podmínky, ale nic nebrání lyžařům, aby tam šli, protože nemovitost sousedí s veřejnými pozemky v národním lese Mount Baker-Snoqualmie.
Frank DeBerry, prezident a generální ředitel střediska, uvedl, že všech šest mužů drželo skipasy do kopce, což znamená, že byli registrováni u lyžařské hlídky, podíleli se na orientaci na to, jak a kde se dostat k backcountry lyžování přes majetek střediska a byli povinni zkontrolovat sněhové podmínky před jejich výletem.
