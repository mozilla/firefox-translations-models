Test molekulární diagnostiky dokáže detekovat variantu Omicronu za 20 minut: zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omikronu.
Vývoj technologie byl v současné době dokončen a očekává se, že bude nějakou dobu trvat, než se komercializace vyvine.
POSTECH oznámil 10., že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicronu za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které 26-32 mutací ve špičce, která se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na bázi s jedním nukleotidem, takže může detekovat "Stealth Omicron", které je obtížné detekovat testy PCR.
V současné době používají Korejská centra pro kontrolu a prevenci nemocí tři metody detekce variant COVID-19: celogenomovou analýzu, analýzu cílové DNA (mutace, jako je spike protein) a test PCR.
V případě varianty Delta může být detekován současným PCR testem, ale Omicron nemůže.
Nově vyvinutá technologie tentokrát není sekvenační metoda, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie screenuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobovala reakce vázající nukleové kyseliny pouze tehdy, když existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicronu" se oba geny N a S potvrdily jako pozitivní, což ztěžuje odlišení od jiných variant.
Molekulární diagnostická technologie pracuje v jiných mechanismech než PCR a efektivně detekuje variantu Omicronu.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, může nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, a proto může jednoduše a snadno vyrábět diagnostické soupravy.
Vzhledem k tomu, že metoda může vyvinout diagnostickou soupravu za 4 dny, očekává se, že bude rychle reagovat i v případě, že se v budoucnu objeví nová varianta nebo virus.
"Doufám, že odhalení této technologie nám pomůže vrátit se k normálnímu každodennímu životu co nejdříve," řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které se mohou objevit po COVID-19.
Tato technologie je v současné době před komercializací.
Může však být použit jako pomocný prostředek v současných situacích, kdy nebyl vyvinut PCR test na Omikron.
Profesor Lee řekl: "Myslím, že tato technologie bude blízko komercializaci v druhé polovině příštího roku po klinických zkouškách.
Důvodem, proč tuto technologii zveřejňuji, je sdílet ji s ostatními, aby vyvinuli lepší technologie pro překonání COVID-19 a umožnili nerozvinutým zemím také analyzovat varianty COVID-19."
