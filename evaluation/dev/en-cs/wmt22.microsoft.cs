Lavina byla hlášena kolem 10:50 v oblasti Silver Basin v Crystal Mountain, která se nachází asi 85 mil (137 kilometrů) jihovýchodně od Seattlu, řekl seržant Darren Moss z oddělení šerifa okresu Pierce.
Totožnost muže, který zemřel, nebyla zveřejněna, ale úřady tvrdí, že nedýchal poté, co byl vytažen ze sněhu a nepřežil navzdory úsilí jiného lyžaře o CPR.
