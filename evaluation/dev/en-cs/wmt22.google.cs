Test molekulární diagnostiky dokáže detekovat omikronovou variantu za 20 minut: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicron.
Vývoj technologie je v současné době dokončen a očekává se, že komercializace bude nějakou dobu trvat.
POSTECH 10. dne oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul technologii molekulární diagnostiky, která dokáže detekovat variantu Omicron za pouhých 20-30 minut, a výsledky zveřejní online.
Omicron je varianta, ve které 26-32 mutací v hrotu, který se používá k infekci buněk virem COVID-19.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže dokáže detekovat „Stealth Omicron“, které je obtížné detekovat pomocí PCR testů.
V současné době používá Korea Centers for Disease Control and Prevention tři metody k detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace, jako je spike protein) a test PCR.
V případě varianty Delta ji lze detekovat aktuálním PCR testem, ale Omicron nikoli.
Nově vyvinutá technologie tentokrát není sekvenační metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostickou technologií.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobovala reakce na vazbu nukleových kyselin pouze v případě, že existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron silný signál pro N geny v testech PCR, ale má slabý signál pro S geny.
V případě &quot;Stealth Omicron&quot; potvrdily oba geny N i S pozitivitu, což ztěžuje odlišení od jiných variant.
Molekulární diagnostická technologie pracuje v odlišných mechanismech než PCR a účinně detekuje variantu Omicron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, dokáže nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Tato technologie navíc nepotřebuje specializované vybavení, takže lze jednoduše a snadno vyrobit diagnostické soupravy.
Protože metoda dokáže vyvinout diagnostickou soupravu za 4 dny, očekává se, že bude reagovat rychle, i když se v budoucnu objeví nová varianta nebo virus.
