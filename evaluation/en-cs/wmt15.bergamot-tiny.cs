V takové situaci jim mohu odpustit, že nevyhráli.
Kdybychom tak hráli od začátku sezony, pak jsem přesvědčen, že bychom neprohráli," řekl Lébra.
V Opavě Sigma zastavila počáteční tlak domácího týmu, a pak to hru vyrovnalo, a ve druhém poločase to už bylo lepší.
Po vítězství vynaložili Kalvodovi svěření hráči úsilí, ale nedosáhli žádných gólů, a tak nemohli vyhrát.
"Výsledek není to, co jsme očekávali.
Ale pro naprostou spokojenost jsme propásli jen jednu proměnlielnou příležitost," řekl Lébra, který se však domnívá, že změna přístupu hráčů byla způsobena pouze jeho hrozbou.
"Bylo tam určitě několik aspektů.
Jak z mé strany, tak ze strany trenéra nebo fanoušků," řekl Lébra.
Sigma musí předvídat tlak.
Po zápase v Opavě se hráči k událostem z minulého týdne příliš nevyjádřili.
"Snažím se tyto věci hodit úplně za hlavu.
Snažil jsem se situaci neuznat," řekl mladý brankář Michal Reichl a zavrtěl hlavou.
Lébra z minulého týdne zůstalo zcela bez komentáře ofenzivního hráče Václavačkačka, který nesplnil největší příležitost Sigmy v sobotním zápase.
Nakonec o tom však mluvil jen trochu.
"Vždycky budeme pod tlakem.
Každý nás chce roztrhat na kusy.
Dnes jsme ukázali, že pokud budeme bojovat a nakopat všechny zadek, pak to bude fungovat," řekl.
Varování si vzali k srdci.
Ani trenér Leoš Kalvoda neváhal říci, že příprava na zápas není nejsnazší, vzhledem k atmosféře, která byla minulý týden přítomna v Olomouci.
