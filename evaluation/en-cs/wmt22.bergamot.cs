Test Molekulární Diagnóza Může Detekce Omicron Variant Za 20 Minutů: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicron.
Vývoj technologií byl v současné době dokončen a očekává se, že bude nějakou dobu trvat nějakou dobu pro komercializaci.
POSTECH oznámil 10., že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, při které 26-32 mutací v hrotu, která se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na bázi jednokladidla, takže může detekovat "Stealth Omicron", které lze odhalit PCR testy.
V současné době Korea Centers for Disease Control and Prevention používá tři metody k detekci variant COVID-19: celogenomová analýza, analýza cílené DNA (mutace, jako je hrotový protein) a PCR test.
V případě varianty Delta může být detekován aktuálním PCR testem, ale Omicron nemůže.
Nově vyvinutá technologie tentokrát není sekvenční metoda, která by četla sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobila reakce vázající nukleovou kyselinou pouze tehdy, když existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee, Omicron má silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" jak N, tak S geny potvrdily pozitivní, což ztěžuje odlišení od jiných variant.
Molekulární diagnostická technologie funguje v různých mechanismech od PCR a efektivně detekuje variantu Omicron.
Na rozdíl od konvenčních technologií, která obvykle zpracovává až 96 vzorků na jedno zařízení, může nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializovaná zařízení, a proto může vyrobit diagnostické soupravy jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout sadu pro diagnostiku za 4 dny, očekává se, že bude reagovat rychle, i když se v budoucnu objeví nová varianta nebo virus.
