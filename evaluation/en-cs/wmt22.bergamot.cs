"Rozhovor s upírem" autorka Anne Riceová zemřela ve věku 80 let
Zemřela kvůli komplikacím vyplývajícím z mrtvice, řekl Christopher Rice.
Riceovým největším úspěchem byl její první román "Rozhovor s upírem", který vyšel v roce 1976 a představil postavu upíra Lestata, který byl ústřední postavou 13-knihy série Chronicles, z nichž poslední byla vydána v roce 2018.
"Měla jsem představu o Lestatovi jako o muži akce, muže, který mohl dělat věci, které jsem nemohl dělat," řekl Rice v rozhovoru na Southern Illinois University v roce 2010.
"Rozhovor s upírem" byl natočen jako úspěšný celovečerní film v roce 1994, který pomohl znovu zaměnit zájem o žánr upírů, který pokračoval s televizním seriálem "The Vampire Diaries" a filmovou sérií "Twilight".
Přestože prožila většinu svého života v Kalifornii, Riceová byla rodačkou z New Orleans a podle své webové biografie nastavila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
