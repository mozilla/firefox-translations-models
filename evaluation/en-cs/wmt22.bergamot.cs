POSTECH na desáté oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omikron za pouhých 20-30 minut a zveřejní výsledky online.
Omikron je varianta, ve které se 26-32 mutací ve špičce, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na bázi jednoho jaderného západu, takže může detekovat "Stealth Omikron", které je obtížné detekovat PCR testy.
V současné době Korea Centers for Disease Control and Prevention používá tři metody k detekci variant COVID-19: celogenomovou analýzu, cílovou DNA (mutace, jako je spike protein) a PCR test.
V případě varianty Delta ji lze detekovat současným PCR testem, ale Omikron nemůže.
Nově vyvinutá technologie tentokrát není sekvenační metodou, která čte DNA nebo RNA sekvence, ale molekulární diagnostická technologie.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobila nukleovou reakci vazebník kyselinu pouze tehdy, když existuje COVID-19 RNA, což umožňuje rychlou detekci.
Podle profesora Lee má Omikron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" potvrdily jak geny N, tak S pozitivní, což ztěžovalo odlišení od jiných variant.
Molekulární diagnostická technologie pracuje v různých mechanismech z PCR a detekuje efektivně variantu Omicron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, může nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, takže může vyrábět diagnostické sady jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout diagnostickou sadu za 4 dny, očekává se, že bude reagovat rychle, i když v budoucnu nastane nová varianta nebo virus.
