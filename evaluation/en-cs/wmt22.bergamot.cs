"Interview with the Vampire" autorka Anne Rice zemřela ve věku 80 let
Zemřela kvůli komplikacím způsobeným mrtvicí, řekl Christopher Rice.
Největším úspěchem Rice byl její první román "Interview with the Vampire", který byl publikován v roce 1976 a představil postavu upíra Lestata, který by byl ústřední postavou v sérii 13-knih Chronicles, z nichž poslední byla vydána v roce 2018.
"Měl jsem představu o Lestatovi jako o muži činu, muži, který by mohl dělat věci, které jsem nemohl udělat," řekl Rice v rozhovoru na Southern Illinois University v roce 2010.
"Interview with the Vampire" byl v roce 1994 natočen do úspěšného celovečerního filmu, který pomohl znovu zažehnout zájem o žánru upírů, který pokračoval v televizním seriálu "The Vampire Diaries" a "Twilight" filmové série.
Ačkoli žila většinu svého života v Kalifornii, Rice byla rodačkou z New Orleans a podle své webové fotografie tam nastavila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleans s veřejným památníkem plánovaným na příští rok.
