Takže je přinejmenším nejasné, zda si bývalý prezident může nárokovat privilegium, nemluvě o tom, zda by tvrzení uspělo.
Navíc odvolání Nejvyššího soudu kvůli pokusu tehdejšího prezidenta Richarda Nixona vyhnout se dodržování předpisů předvoláním ukazuje, že vágní tvrzení o privilegiích nebude a může být překonáno specifickou potřebou důkazů.
Soud poznamenal v roce 1974 Spojené státy v. Nixon:
...když je důvod pro uplatnění privilegií, které mají být určeny k použití v trestním řízení, založen pouze na všeobecném zájmu o důvěrnost, nemůže převahovat nad základními požadavky řádného právního procesu při spravedlivém výkonu trestního soudnictví.
