Autor: Florence Tanová
Zimbabwe začne do března provozovat novou jednotku ve své jediné uhelné elektrárně, říká náměstek ministra energetiky země a poskytne úlevu milionům občanů otřásaným častými výpadky elektřiny v posledních měsících.
Nová jednotka elektrárny Hwange zvedne instalovaný výkon afrického národa o více než 14% až 2400 megawattů.
Očekává se, že další jednotka bude uvedena do provozu brzy poté, řekl Magna Mudyiwa, aniž by uvedl časovou osu.
Méně než polovina z 16 milionů občanů Zimbabwe má přístup k elektřině a globální tlak na financování nové uhelné kapacity omezil schopnost země zaplnit chronické výpadky proudu, které v posledních dnech vydržely až 18 hodin.
"Máme kapacitu generovat až 2100 megawattů (MW) z našich zdrojů energie, ale v tuto chvíli generujeme mnohem méně než to... asi 1000MW," řekl Mudyiwa agentuře Reuters.
"Ale naše poptávka po elektřině je asi 1700MW, takže máme vážný deficit," řekla.
Nedostatek srážek vedl k poklesu výroby vodní energie, zatímco účinnost jediné, desítky let staré uhelné společnosti se v průběhu času prudce snížila, zatímco poptávka po energii v posledních letech vzrostla kvůli vyšší těžbě a zemědělské činnosti.
Mezinárodní měnový fond počítá nedostatek elektřiny jako jeden z hlavních faktorů, které zatěžují vyhlídky růstu Zimbabwe.
