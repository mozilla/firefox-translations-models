„Rozhovor s upírem“ Anne Riceová zemřela ve věku 80 let
Zemřela kvůli komplikacím způsobeným mrtvicí, řekl Christopher Rice.
Největším úspěchem Riceové byl její první román „Interview with the Vampire“, který vyšel v roce 1976 a představil postavu upíra Lestata, který měl být ústřední postavou v sérii 13 knih Chronicles, z nichž poslední vyšla v roce 2018.
„Měl jsem představu o Lestatovi jako o muži činu, o muži, který dokáže věci, které já dělat nemůžu,“ řekl Rice v rozhovoru na Southern Illinois University v roce 2010.
„Souvislost s upírem“ byl v roce 1994 zfilmován jako úspěšný celovečerní film, který pomohl znovu probudit zájem o upíří žánr, který pokračoval s televizním seriálem Upíří deníky a filmovým seriálem Stmívání.
Přestože Riceová prožila většinu svého života v Kalifornii, byla rodačkou z New Orleans a podle svého životopisu na svých internetových stránkách uvedla mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleansu, s veřejným památníkem plánovaným na příští rok, řekl.
