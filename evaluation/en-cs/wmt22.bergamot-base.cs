Joe Biden Laments se nedaří zastavit globální oteplování po smrtících tornádech
Prezident Joe Biden si v sobotu posteskl nad neschopností světa zastavit globální oteplování poté, co se vypořádal se smrtícími tornády, která se přehnala několika státy.
„Všichni víme, že když se otepluje, všechno je intenzivnější.
Všechno,“ řekl.
„A tady to má zřejmě nějaký dopad.“
Nejméně 30 tornád bylo hlášeno v šesti různých státech, což způsobilo rozsáhlou devastaci, a očekává se, že v důsledku bouře zahyne více než 100 lidí.
Prezident řekl, že nezná plný rozsah toho, jak globální oteplování přispělo ke smrtícím bouřím, které označil za jedno z „největších tornád v historii“.
Řekl, že přiměje Agenturu pro ochranu životního prostředí, aby to prošetřila.
„Jediné, co vím, je, že intenzita počasí na celé ploše má určitý dopad v důsledku oteplování planety,“ řekl Biden.
Prezident pochválil reportéra, který se ho ptal na změnu klimatu.
„Jako obvykle se ptáš vždycky na to nejlepší,“ řekl s kysejícím úsměvem.
„Jak se s tím vypořádáme?“ pokračoval.
„Částečně si uvědomujeme, že pravděpodobnost menších katastrof způsobených počasím, bez pokračujícího pohybu v boji s globálním oteplováním, k tomu prostě nedojde.“
Biden řekl, že byl šokován rekordními lesními požáry v zemi během roku 2021, a vyjádřil znepokojení nad tím, že globální oteplování je hlavním přínosem.
„Takže musíme jednat,“ řekl.
Biden řekl, že prvním krokem bylo zachránit životy a starat se o rodiny, které byly zasaženy bouřemi.
„To ti slibuji.
Cokoliv je potřeba.
Ať je potřeba cokoliv, federální vláda to dodá,“ řekl Biden.
Řekl, že bude pokračovat v pečlivém sledování obnovy bouře a udělá vše, co bude federální vláda potřebovat.
„Chci, aby to věděli lidé ve všech těchto státech.
My se přes to dostaneme.
Společně to zvládneme a federální vláda neodejde,“ řekl.
„Tohle je jedna z těch okamžiků, kdy nejsme demokraté nebo republikáni.“
Prezident řekl, že navštíví oblast zasažené bouří, až bude jasné, že se nebude stavět do cesty místním záchranným pracím.
„Mám v plánu jít,“ řekl.
