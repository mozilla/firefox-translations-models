Molekulární diagnostický test může detekovat Omicron Variant za 20 minut: Sestava
Korejští výzkumníci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicronu.
Vývoj technologií byl v současné době dokončen a očekává se, že bude nějakou dobu trvat na komercializaci.
POSTECH 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicronu za pouhých 20-30 minut a výsledky zveřejní na internetu.
Omicron je varianta, ve které se mutuje 26-32 mutací v hrotovém výpadku, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže může detekovat „Stealth Omicron“, které jsou obtížně detekovatelné pomocí PCR testů.
V současné době Korejská centra pro kontrolu a prevenci nemocí používají tři metody pro detekci variant COVID-19: celogenomovou analýzu, analýzu cílové DNA (mutace, jako je protein hrotového tělesa) a PCR test.
V případě varianty Delta může být detekována aktuálním PCR testem, ale Omicron ne.
Nově vyvinutá technologie tentokrát není metodou sekvenování, která čte sekvence DNA nebo RNA, ale molekulárně diagnostická technologie.
Stávající technologie zkoumá pouze specifické oblasti viru, ale molekulárně diagnostická technologie byla navržena tak, aby způsobovala reakce vázající nukleovou kyselinu pouze tehdy, pokud existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Leeho má Omicron v PCR testech silný signál pro N geny, ale má slabý signál pro S geny.
V případě „Stealth Omicron“ se oba N a S geny potvrdily pozitivně, takže je obtížné se odlišit od jiných variant.
Molekulární diagnostická technologie pracuje v různých mechanismech od PCR, detekuje Efektivně Omicronovu variantu.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, dokáže nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, takže může jednoduše a snadno vyrobit diagnostické soupravy.
Vzhledem k tomu, že metoda může vyvinout diagnózu za 4 dny, očekává se, že bude reagovat rychle, i když se v budoucnu objeví nová varianta nebo virus.
„Doufám, že nám odhalení této technologie pomůže co nejdříve se vrátit do normálního každodenního života,“ řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Může však být použit jako pomocný v současných situacích, kdy PCR test pro Omicron nebyl vyvinut.
Profesor Lee řekl: „Myslím, že tato technologie bude blízko ke komercializaci v druhé polovině příštího roku po klinických testech.
Důvodem, proč tuto technologii zveřejňuji, je sdílet ji s ostatními za cílem vyvinout lepší technologie pro překonání COVID-19 a umožnit zaostalým zemím, aby také analyzovaly varianty COVID-19.“
