"Rozhovor s upírem" autor Anne Rice umírá ve věku 80 let
Zemřela kvůli komplikacím vyplývajícím z mrtvice, řekl Christopher Rice.
Největším úspěchem Riceové byl její první román "Rozhovor s upírem", který vyšel v roce 1976 a představil postavu upíra Lestata, který bude ústřední postavou v sérii 13-book Chronicles, z nichž nejnovější byla vydána v roce 2018.
"Měl jsem představu o Lestatovi jako o muži činu, o muži, který by mohl dělat věci, které jsem nemohl udělat," řekl Rice v přednášce na Southern Illinois University v roce 2010.
"Rozhovor s upírem" byl v roce 1994 natočen do úspěšného celovečerního filmu, který pomohl znovu oživit zájem o žánr upírů, který pokračoval televizním seriálem "The Vampire Diaries" a filmovou sérií "Twilight".
Přestože většinu svého života prožila v Kalifornii, Riceová byla rodačkou z New Orleans a podle životopisu na svých webových stránkách tam zasadila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u lůžka své matky, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleans, přičemž příští rok bude plánován veřejný památník, uvedl.
