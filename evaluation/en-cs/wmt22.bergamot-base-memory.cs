Lavina byla hlášena asi v 10:50 v oblasti Stříbrné pánve Crystal Mountain, která se nachází asi 85 mil (137 kilometrů) jihovýchodně od Seattlu, uvedl úřad šerifa okresu Pierce. Darren Mossová.
