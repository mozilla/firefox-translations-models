Vývoj technologií byl v současné době dokončen a očekává se, že bude trvat nějakou dobu, než se bude komercializovat.
Společnost POSTECH 10. oznámila, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omikron za pouhých 20-30 minut a výsledky zveřejní online.
Omikron je varianta, ve které 26-32 mutací ve špičce, která se používá k infekci buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na jednonukleotidové bázi, takže může detekovat "Stealth Omicron", které je obtížné detekovat pomocí PCR testů.
V současné době Korejské centrum pro kontrolu a prevenci nemocí používá tři metody k detekci variant COVID-19: celogenomová analýza, analýza cílové DNA (mutace, jako je spike protein) a PCR test.
V případě varianty Delta ji lze detekovat aktuálním PCR testem, ale Omikron nemůže.
Nově vyvinutá technologie tentokrát není sekvenační metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobovala reakce na vázání nukleových kyselin pouze tehdy, když existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omikron silný signál pro N geny v PCR testech, ale má slabý signál pro geny S.
V případě "Stealth Omicron" se geny N i S potvrdily pozitivní, takže je obtížné odlišit od jiných variant.
Technologie molekulární diagnostiky pracuje v různých mechanismech z PCR, efektivně detekuje variantu Omikron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na jedno zařízení, může nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, a proto může dělat diagnostické sady jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout diagnostickou sadu za 4 dny, očekává se, že bude rychle reagovat, i když se v budoucnu objeví nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit do normálního každodenního života," řekl profesor Lee.
