Když bývalá první dáma Eleanor Rooseveltová předsedala Mezinárodní komisi pro lidská práva, která vypracovala Všeobecnou deklaraci lidských práv, která by byla v roce 1948 přijata Organizací spojených národů jako globální smlouva, Roosevelt a navrhovatelé zahrnovali záruku, že „každý má právo tvořit a vstoupit do odborů pro ochranu svých zájmů“.
