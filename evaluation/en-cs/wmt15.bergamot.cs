Znovuspojení s velmi americkým ideálem, že pracovní práva jsou lidská práva
Kongresmani Keith Ellison a John Lewis navrhli legislativu na ochranu organizování odborů jako občanského práva.
"Jak jít odbory, tak jděte na střední třídu pracovních míst," říká Ellison, minnesotský demokrat, který slouží jako spolupředseda kongresového progresivního výboru.
To je důvod, proč jsem hrdý na to, že mohu předložit zákon o posílení postavení zaměstnanců s ikonou občanských práv Johnem Lewisem.
Tato průlomová legislativa poskytne pracovníkům stejné právní možnosti pro odbory organizující diskriminaci jako pro jiné formy diskriminace - zastavení protiodborových sil v jejich stopách.
Změna zákona o národních pracovních vztazích, aby umožnila pracovníkům, kteří čelí diskriminaci za zapojení do organizace odborů, aby žalovali o spravedlnost u civilních soudů - a shromáždili kompenzační a represivní škody - je zdravá a nezbytná iniciativa.
Ale to rozhodně v radikální iniciativě - alespoň podle amerických standardů.
Vskutku, nejlepší způsob, jak pochopit, co Ellison, Lewis a spolupořadatelé jejich legislativy navrhují, je jako znovuspojení s velmi americkou myšlenkou.
Navzdory bití, které odbory přijaly v posledních letech - ve Wisconsinu, Michiganu a státech po celé zemi - Američané kdysi povzbuzovali země po celém světě, aby přijaly, rozšířily a respektovaly pracovní práva.
V rámci živé paměti milionů Američanů byla doba, kdy tato země prosazovala demokracii, svobodu projevu, svobodu tisku a právo organizovat se stejným dechem.
Když Spojené státy po druhé světové válce obsadily Japonsko, generál Douglas MacArthur a jeho poradci povzbuzovali zemi, aby přijala ústavu, jejímž cílem je zajistit, aby byla militarizovaná autokracie Hideki Tojo nahrazena demokracií.
Plně si uvědomují, že dělníci a jejich odbory hráli roli při formování nového Japonska, zahrnovali jazyk, který výslovně uznal, že „právo pracovníků organizovat se a vyjednávat a kolektivně jednat“.
Když Spojené státy po druhé světové válce okupovaly Německo, generál Dwight David Eisenhower a jeho poradci naléhali na Němce, aby napsali ústavu, která by zajistila, že fašismus Adolfa Hitlera bude nahrazen svalovou demokracií.
Uznávajíce, že dělníci by museli organizovat a nechat vyslyšet svůj hlas v novém národě, Němci obsahovali ustanovení, které výslovně prohlásilo: „Právo vytvořit sdružení na ochranu a zlepšení pracovních a ekonomických podmínek bude zaručeno každému jednotlivci a každému povolání nebo povolání.
