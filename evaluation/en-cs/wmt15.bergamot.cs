Sigma se usmíří s Lébrou, i bez vítězství
Olomouc – hromy a blesky, které spolumajitel Sigmy Olomouc Josef Lébra minulý týden vypustil na své podřízené, jako by skončily.
A to i přesto, že z Opavy nakonec odnesli pouze jeden bod za bezbrankovou remízu.
Proto se šéf týmu trochu smířil s úsilím a bojovností týmu.
Očekává se, že peníze na výplaty brzy přijdou do týmu.
„Zdržím si ještě jeden zápas, jak jsem mluvil minulý týden, ale už teď jsem mnohem optimističtější,“ potvrdil Lébra a vysvětlil, že i když Sigma nevyhrála, hráči ho přesvědčili zlepšenými výkony.
Bojovnost a úsilí, s nimiž jsme hráli hlavně ve druhém poločase, byly přesně to, co chci od hráčů vidět.
V takové situaci jim mohu odpustit, že nevyhráli.
Kdybychom tak hráli od začátku sezony, tak jsem přesvědčená, že bychom neprohráli,“ řekla Lébra.
V Opavě Sigma zastavila počáteční tlak domácího týmu, pak to vyrovnalo hru a ve druhé půli to už bylo lepší.
Po vítězství Kalvodovi pověření hráči vyvíjeli úsilí, ale nedosáhli žádných cílů, a tak nemohli vyhrát.
„Výsledek není takový, jaký jsme očekávali.
Ale pro naprosté uspokojení jsme propásli jen jednu proměněnou příležitost,“ řekl Lébra, který se však domnívá, že změnu v přístupu hráčů způsobila pouze jeho hrozba.
„Určitě to bylo v mnoha ohledech.
Jak z mé strany, tak ze strany trenéra nebo fanoušků,“ řekl Lébra.
Sigma musí předvídat tlak.
Po zápase v Opavě se hráči k událostem z minulého týdne nechtěli příliš vyjadřovat.
„Snažím se tyhle věci nechat úplně za hlavu.
Snažil jsem se tu situaci nepřipustit,“ řekl mladý brankář Michal Reichl a zavrtěl hlavou.
Lébrovo prohlášení z minulého týdne zůstalo zcela bez komentáře urážlivého hráče Václava Vašíčka, který v sobotním zápase nenaplnil největší příležitost Sigmy.
Nakonec však o tom trochu promluvil.
„Vždycky budeme pod tlakem.
Každý nás chce rozdělit.
Dnes jsme předvedli, že když budeme bojovat a nakopat všem zadek, tak to funguje,“ řekl.
Vzali si varování k srdci.
Ani trenér Leoš Kalvoda neváhal říci, že příprava na zápas nebyla nejjednodušší vzhledem k atmosféře, která byla v Olomouci přítomna minulý týden.
Bylo to velmi těžké.
Ale myslím, že si to kluci uvědomili a dnes bylo jasné, že pana pana unesli. Lébřino varování pro srdce.
To je pravda, i když i předtím se nechtěli prát a flákali se,“ řekl olomoucký trenér.
„Bylo to podobné jako dnes v prvních patnácti minutách.
Trvalo to, dokud jsme se nevyrovnali s houževnatostí a neuvědomili si, o čem druhá liga je.
Že to není o fotbale, ale hlavně o boji,“ řekl Kalvoda.
Jediné, co zbývá udělat, je doufat, že v příštích hrách bude Sigmě od začátku fungovat.
„Opravdu bych tomu rád věřil,“ dodal.
