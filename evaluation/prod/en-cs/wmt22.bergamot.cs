Molekulární diagnostika Test Can Detect Omicron Variant In 20 Minutes: Report
Korejští výzkumníci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicronu.
Vývoj technologií byl v současné době dokončen a očekává se, že si vyžádá čas na komercializaci.
POSTECH na desátém oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které 26-32 mutací ve špičce, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat na jednonnocleotidové základně, takže dokáže detekovat „Stealth Omicron“, který je obtížné detekovat testy PCR.
V současné době používá Korea Centra pro kontrolu a prevenci nemocí tři metody pro detekci variant COVID-19, celogenomickou analýzu, cílovou analýzu DNA (mutace, jako je proteinový nárůst) a PCR test.
V případě varianty Delta ji lze zjistit současným PCR testem, ale přípravek Omicron nemůže.
Nově vyvinutá technologie tentokrát není sekvenovací metodou, která čte sekvencí DNA nebo RNA, ale molekulární diagnostickou technologii.
Stávající technologie obrazovky pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobovala reakce váhací s nukleovými kyselinami až v době, kdy existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron v PCR testech silný signál pro geny N, ale má slabý signál pro geny S.
V případě „Stealth Omicron“ potvrdily N i S geny pozitivní, takže bylo obtížné se odlišit od ostatních variant.
Molezulární diagnostická technologie funguje v různých mechanismech od PCR, efektivně detekuje variantu Omicron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na jedno zařízení, může nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované zařízení, takže může jednoduše a snadno vyrobit diagnostické soupravy.
Vzhledem k tomu, že metoda může vyvinout diagnózu za 4 dny, očekává se, že bude reagovat rychle, i když se v budoucnu objeví nová varianta nebo virus.
„Doufám, že zveřejnění této technologie nám pomůže vrátit se co nejdříve do normálního každodenního života,“ řekl profesor Lee.
Pokusíme se rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Může však být použit jako pomocný prostředek v současných situacích, kdy nebyl test PCR pro přípravek Omicron vyvinut.
