'Interview s upírem' autorka Anne Rice umírá ve věku 80 let
Zemřela kvůli komplikacím způsobeným mozkovou mrtvicí, řekl Christopher Rice.
Největším úspěchem byla Riceová její první román „Interview with the Vampire“, který vyšel v roce 1976 a představil postavu upíra Lestata, který se stane ústřední postavou v třináctikničkových seriálech Chronicles, z nichž poslední vyšla v roce 2018.
„Měl jsem představu o Lestatovi jako o muži činu, o muži, který dokázal věci, které jsem nemohl dělat,“ řekl Rice v přednášce na jižní Illinoisské univerzitě v roce 2010.
„Interview with the Vampire“ se stal úspěšný celovečerní film v roce 1994, což pomohlo znovu se zaměstnáit zájem o upíří žánr, který pokračoval v televizním seriálu Vampire Diaries a filmové sérii „Twilight“.
I když žila většinu svého života v Kalifornii, Rice byl rodákem z New Orleans a podle jejího životopisu na webu umístila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčiny postele, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleans, s veřejným památníkem plánovaným na příští rok, řekl.
